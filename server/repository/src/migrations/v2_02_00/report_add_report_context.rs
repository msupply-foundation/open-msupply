use crate::{migrations::sql, StorageConnection};

pub(crate) fn migrate(connection: &StorageConnection) -> anyhow::Result<()> {
    if cfg!(feature = "postgres") {
        sql!(
            connection,
            r#"
            ALTER TYPE context_type ADD VALUE IF NOT EXISTS 'REPORT';
            "#
        )?;
    }

    Ok(())
}
